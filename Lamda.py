# Normal approach for finding the square of a number
def square(n):
    return n*n
print(square(6))

# Using Lambda function "Strainght foreward" we can easily find the square of the number

square = lambda x : x*x
print(square(6))

divide = lambda x, y : x//y
print(divide(10,2))

cube = lambda x: x*x*x
print(cube(3.5))
